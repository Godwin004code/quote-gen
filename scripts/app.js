const imgOutput = document.querySelector('img')
const quotesInput = document.getElementById('quotes-input')
const quotesBtn = document.querySelector('#quotes-btn')
const output = document.querySelector('.output')

quotesBtn.addEventListener('click', getQuotes)

function getQuotes() {
    let val = quotesInput.value
    if(val === "") {
        showError()
    } else {
   
    let temp = Quotes.map((quote) => {
        
        return (
            `<div class="img-text" data-aos="fade-up" data-aos-duration="1000" data-aos-easing="ease-in-quad">
            <div class="img">
            <img src="${quote.img}" />
            </div>
            <div class="text">
                <p>
                <i class="fas fa-quote-left"></i> ${quote.text}  <i class="fas fa-quote-right"></i> </p>
                <h2>${quote.author}</h2>
            </div>
            
            </div>
           
            `
        )
    })
    let item = temp.slice(0, val).join('')
    output.innerHTML = `${item}`
    
}
setTimeout(() => showError, 1000)
}

function showError() {
    output.innerHTML = `<div class="error">Please input a value
        <div class="line"></div>
        </div>`
}
